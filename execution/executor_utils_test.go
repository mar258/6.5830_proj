// insertRows is a genericx helper to insert raw expressions into a table.
// It handles the creation and draining of an InsertExecutor.
func insertRows(t *testing.T, th *TableHeap, rows [][]planner.Expr, indexes []indexing.Index) {
	valuesNode := planner.NewValuesNode(rows, th.StorageSchema().GetFieldTypes())
	valuesExec := NewValuesExecutor(valuesNode)

	insertNode := planner.NewInsertNode(th.oid, valuesNode)
	insertExec := NewInsertExecutor(insertNode, valuesExec, th, indexes)

	require.NoError(t, insertExec.Init(NewExecutorContext(nil)))

	for insertExec.Next() {
	}

	require.NoError(t, insertExec.Error())
	require.NoError(t, insertExec.Close())
}

// Schema: (sale_id int, revenue int, cost int, region string, category string)
func setupSalesTable(t *testing.T, bp *storage.BufferPool) *TableHeap {
	schema := &catalog.Table{
		Oid:  999,
		Name: "sales",
		Columns: []catalog.Column{
			{Name: "sale_id", Type: common.IntType},
			{Name: "revenue", Type: common.IntType},
			{Name: "cost", Type: common.IntType},
			{Name: "region", Type: common.StringType},
			{Name: "category", Type: common.StringType},
		},
	}

	th, err := NewTableHeap(schema, bp, storage.NoopLogManager{}, nil)
	require.NoError(t, err)

	return th
}

func insertSalesData(t *testing.T, th *TableHeap, indexes []indexing.Index, numRows int) {
	var rows [][]planner.Expr
	for i := 0; i < numRows; i++ {
		region := "US"
		if i%2 != 0 {
			region = "EU"
		}

		category := "Clothing"
		if i%3 == 0 {
			category = "Electronics"
		} else if i%3 == 1 {
			category = "Books"
		}

		row := []planner.Expr{
			planner.NewConstantValueExpression(common.NewIntValue(int64(i))),       // id
			planner.NewConstantValueExpression(common.NewIntValue(int64(i * 200))), // revenue
			planner.NewConstantValueExpression(common.NewIntValue(int64(i * 100))), // cost
			planner.NewConstantValueExpression(common.NewStringValue(region)),      // region
			planner.NewConstantValueExpression(common.NewStringValue(category)),    // category
		}
		rows = append(rows, row)
	}

	insertRows(t, th, rows, indexes)
}

// Shipments Schema: (ship_id, sale_id, carrier)
func setupShipmentTable(t *testing.T, bp *storage.BufferPool) *TableHeap {
	shipCols := []catalog.Column{
		{Name: "ship_id", Type: common.IntType},
		{Name: "sale_id", Type: common.IntType}, // Join Key
		{Name: "carrier", Type: common.StringType},
	}
	schema := &catalog.Table{Oid: 2, Name: "shipments", Columns: shipCols}
	th, err := NewTableHeap(schema, bp, storage.NoopLogManager{}, nil)
	require.NoError(t, err)
	return th
}

// insertShipmentData populates the shipment table.
// ship_id: 100 + 2 * i
// sale_id: i (Matches the IDs generated by insertSalesData)
// carrier: Rotates FedEx/UPS/DHL/USPS
func insertShipmentData(t *testing.T, th *TableHeap, indexes []indexing.Index, numRows int) {
	var rows [][]planner.Expr
	carriers := []string{"FedEx", "UPS", "DHL", "USPS"}

	for i := 0; i < numRows; i++ {
		row := []planner.Expr{
			planner.NewConstantValueExpression(common.NewIntValue(int64(100 + 2*i))), // ship_id
			planner.NewConstantValueExpression(common.NewIntValue(int64(i))),         // sale_id
			planner.NewConstantValueExpression(common.NewStringValue(carriers[i%4])), // carrier
		}
		rows = append(rows, row)
	}
	insertRows(t, th, rows, indexes)
}

