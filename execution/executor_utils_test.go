// verifyIndexMatch checks if the index contains an entry for the specific key values pointing to targetRid.
func verifyIndexMatch(t *testing.T, idx indexing.Index, keyValues []common.Value, targetRid common.RecordID, present bool) {
	keySchema := idx.Metadata().KeySchema
	keyTuple := storage.FromValues(keyValues...)
	keyRaw := make([]byte, keySchema.BytesPerTuple())
	keyTuple.WriteToBuffer(keyRaw, keySchema)

	rids, err := idx.ScanKey(idx.Metadata().AsKey(keyRaw), nil, nil)
	require.NoError(t, err)

	found := false
	for _, r := range rids {
		if r == targetRid {
			found = true
			break
		}
	}

	assert.Equal(t, present, found, "Unexpected index entry for Key: %v, RID: %v", keyValues, targetRid)
}

func checkJoinResult(t *testing.T, joinTree Executor, expected []storage.Tuple) {
	// Build frequency map of expected results
	tupleDesc := storage.NewRawTupleDesc(joinTree.PlanNode().OutputSchema())
	buffer := make([]byte, tupleDesc.BytesPerTuple())

	expectedMap := make(map[string]int)
	for _, tup := range expected {
		tup.WriteToBuffer(buffer, tupleDesc)
		expectedMap[string(buffer)]++
	}

	count := 0
	for joinTree.Next() {
		joinTree.Current().WriteToBuffer(buffer, tupleDesc)
		key := string(buffer)
		remaining, ok := expectedMap[key]
		if !ok || remaining == 0 {
			assert.Fail(t, fmt.Sprintf("Unexpected or extra tuple in join result"))
			return // early stop on first mismatch
		}
		expectedMap[key]--
		count++
	}

	// Verify all expected tuples were found
	missingCount := 0
	for _, remaining := range expectedMap {
		missingCount += remaining
	}
	assert.Equal(t, 0, missingCount, "Some expected tuples were missing from the result")
	assert.Equal(t, len(expected), count, "Total result count mismatch")
}

// simpleNestedLoopJoin calculates the expected join result using an in-memory nested loop.
// It iterates over the provided heaps, merges tuples, and evaluates the predicate.
// This serves as the "Ground Truth" for verification.
func simpleNestedLoopJoin(t *testing.T, leftHeap *TableHeap, rightHeap *TableHeap, predicate planner.Expr) []storage.Tuple {
	var results []storage.Tuple

	leftDesc := leftHeap.StorageSchema()
	rightDesc := rightHeap.StorageSchema()
	joinedTypes := append(leftDesc.GetFieldTypes(), rightDesc.GetFieldTypes()...)
	joinedDesc := storage.NewRawTupleDesc(joinedTypes)

	// Allocate buffers for the iterators
	leftBuf := make([]byte, leftDesc.BytesPerTuple())
	rightBuf := make([]byte, rightDesc.BytesPerTuple())
	outBuf := make([]byte, joinedDesc.BytesPerTuple())

	// Outer Loop (Left Table)
	// We pass nil for transaction to bypass locking for this test verification helper
	leftIter, err := leftHeap.Iterator(nil, transaction.LockModeS, leftBuf)
	require.NoError(t, err)
	defer leftIter.Close()

	for leftIter.Next() {
		leftTuple := storage.FromRawTuple(leftIter.CurrentTuple(), leftDesc, leftIter.CurrentRID())
		rightIter, err := rightHeap.Iterator(nil, transaction.LockModeS, rightBuf)
		require.NoError(t, err)
		for rightIter.Next() {
			rightTuple := storage.FromRawTuple(rightIter.CurrentTuple(), rightDesc, rightIter.CurrentRID())

			// Merge tuples into a temporary buffer
			storage.MergeTuples(outBuf, joinedDesc, leftTuple, rightTuple)
			joinedTuple := storage.FromRawTuple(outBuf, joinedDesc, common.RecordID{})
			// Evaluate the Join Predicate
			// ExprIsTrue returns true only if the result is Non-Null and Non-Zero
			if planner.ExprIsTrue(predicate.Eval(joinedTuple)) {
				results = append(results, joinedTuple.DeepCopy(joinedDesc))
			}
		}
		rightIter.Close()
	}

	return results
}

// checkAggregateResult verifies that the output of the executor matches the expected tuples.
func checkAggregateResult(t *testing.T, aggExec Executor, expected []storage.Tuple) {
	tupleDesc := storage.NewRawTupleDesc(aggExec.PlanNode().OutputSchema())
	buffer := make([]byte, tupleDesc.BytesPerTuple())

	expectedMap := make(map[string]int)
	for _, tup := range expected {
		tup.WriteToBuffer(buffer, tupleDesc)
		expectedMap[string(buffer)]++
	}

	count := 0
	for aggExec.Next() {
		aggExec.Current().WriteToBuffer(buffer, tupleDesc)
		key := string(buffer)
		remaining, ok := expectedMap[key]
		if !ok || remaining == 0 {
			assert.Fail(t, fmt.Sprintf("Unexpected or extra tuple in aggregate result"))
			return
		}
		expectedMap[key]--
		count++
	}

	require.NoError(t, aggExec.Error())

	missingCount := 0
	for _, remaining := range expectedMap {
		missingCount += remaining
	}
	assert.Equal(t, 0, missingCount, "Some expected tuples were missing from the result")
	assert.Equal(t, len(expected), count, "Total result count mismatch")
}
